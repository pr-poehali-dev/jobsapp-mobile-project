export const ALL_REGIONS = Object.keys(RUSSIAN_CITIES);

export const getAllCities = (): string[] => {
  const cities: string[] = [];
  Object.values(RUSSIAN_CITIES).forEach(regionCities => {
    cities.push(...regionCities);
  });
  return Array.from(new Set(cities)).sort();
};

export const getCitiesByRegion = (region: string): string[] => {
  return RUSSIAN_CITIES[region as keyof typeof RUSSIAN_CITIES] || [];
};

export const findRegionByCity = (city: string): string | null => {
  for (const [region, cities] of Object.entries(RUSSIAN_CITIES)) {
    if (cities.includes(city)) {
      return region;
    }
  }
  return null;
};
